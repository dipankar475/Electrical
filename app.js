function renderRiskRegister() {
  const listContainer = document.getElementById('risk-register-rows');
  if (!listContainer) return;

  // 1. Calculate general stats
  let total = riskRegister.length;
  let high = 0, med = 0, low = 0;
  
  riskRegister.forEach(r => {
    if (r.r1 >= 15) high++;
    else if (r.r1 >= 6) med++;
    else low++;
  });

  document.getElementById('risk-stat-total').textContent = total;
  document.getElementById('risk-stat-high').textContent = high;
  document.getElementById('risk-stat-medium').textContent = med;
  document.getElementById('risk-stat-low').textContent = low;

  // Update sidebar counter
  const sbCount = document.getElementById('sb-risk-count');
  if (sbCount) {
    sbCount.textContent = total + ' Risks';
    sbCount.className = 'sidebar-score ' + (high > 0 ? 'score-bad' : med > 0 ? 'score-warn' : 'score-good');
  }

  // 2. Count matrix occurrences
  const matrixCounts = {};
  for (let l = 1; l <= 5; l++) {
    matrixCounts[l] = {};
    for (let s = 1; s <= 5; s++) {
      matrixCounts[l][s] = 0;
    }
  }

  riskRegister.forEach(r => {
    const l = parseInt(r.l1) || 1;
    const s = parseInt(r.s1) || 1;
    if (matrixCounts[l] && matrixCounts[l][s] !== undefined) {
      matrixCounts[l][s]++;
    }
  });

  // Update cells visual labels
  document.querySelectorAll('.risk-matrix-cell').forEach(cell => {
    const l = parseInt(cell.getAttribute('data-l'));
    const s = parseInt(cell.getAttribute('data-s'));
    const count = matrixCounts[l][s];
    cell.setAttribute('data-count', count > 0 ? `(${count})` : '');
    cell.innerHTML = `L${l}xS${s}<br><span style="font-size:10px; font-weight:bold; color:var(--text);">${count > 0 ? count + ' items' : ' '}</span>`;
    
    // Add active filter class
    if (riskMatrixFilter && riskMatrixFilter.l === l && riskMatrixFilter.s === s) {
      cell.classList.add('active-cell');
    } else {
      cell.classList.remove('active-cell');
    }
  });

  // 3. Filter register table
  const searchVal = document.getElementById('risk-search').value.toLowerCase().trim();
  const categoryVal = document.getElementById('risk-filter-category').value;

  const filtered = riskRegister.filter(r => {
    if (searchVal) {
      const matchSearch = r.activity.toLowerCase().includes(searchVal) ||
                          r.hazard.toLowerCase().includes(searchVal) ||
                          r.standard.toLowerCase().includes(searchVal);
      if (!matchSearch) return false;
    }
    if (categoryVal && r.category !== categoryVal) return false;
    if (riskMatrixFilter) {
      if (parseInt(r.l1) !== riskMatrixFilter.l || parseInt(r.s1) !== riskMatrixFilter.s) {
        return false;
      }
    }
    return true;
  });

  // 4. Render filtered rows
  if (filtered.length === 0) {
    listContainer.innerHTML = `
      <tr>
        <td colspan="11" style="text-align:center; padding:32px; color:var(--text-muted); font-family:'IBM Plex Mono',monospace;">
          No hazard assessment records found matching current filters.
        </td>
      </tr>
    `;
    return;
  }

  listContainer.innerHTML = filtered.map(r => {
    const r1Badge = r.r1 >= 15 ? 'badge-high' : r.r1 >= 6 ? 'badge-med' : 'badge-low';
    const r2Badge = r.r2 >= 15 ? 'badge-high' : r.r2 >= 6 ? 'badge-med' : 'badge-low';
    const statusClass = r.status === 'Open' ? 'status-open' : r.status === 'In Progress' ? 'status-progress' : 'status-controlled';
    const categoryLabel = {
      electrical: 'Elect',
      fire: 'Fire',
      chemical: 'Chem',
      environmental: 'Env',
      ohs: 'OHS',
      energy: 'Energy'
    }[r.category] || r.category;

    return `
      <tr>
        <td><strong style="font-family:monospace; color:var(--accent);">${r.id}</strong></td>
        <td><span class="badge" style="font-size:8px; border-color:var(--border); background:rgba(255,255,255,0.02); text-transform:uppercase;">${categoryLabel}</span></td>
        <td><strong>${r.activity}</strong></td>
        <td style="font-size:12px; color:var(--text-dim); line-height:1.4;">${r.hazard}</td>
        <td style="font-family:monospace; font-size:10px; color:var(--purple);">${r.standard}</td>
        <td>
          <span class="risk-badge ${r1Badge}">L${r.l1}xS${r.s1} = ${r.r1}</span>
        </td>
        <td style="font-size:12px; color:var(--text-muted);">${r.controls || '--'}</td>
        <td style="font-size:12px; color:var(--text-dim);">${r.actions || '--'}</td>
        <td>
          <span class="risk-badge ${r2Badge}">L${r.l2}xS${r.s2} = ${r.r2}</span>
        </td>
        <td><span class="status-pill ${statusClass}">${r.status}</span></td>
        <td style="text-align:center;">
          <div style="display:flex; gap:6px; justify-content:center;">
            <button class="btn btn-outline" style="font-size:10px; padding:4px 8px; border-color:var(--accent); color:var(--accent);" onclick="openRiskModal('${r.id}')">Edit</button>
            <button class="btn btn-outline" style="font-size:10px; padding:4px 8px; border-color:var(--danger); color:var(--danger);" onclick="deleteRiskItem('${r.id}')">Del</button>
          </div>
        </td>
      </tr>
    `;
  }).join('');
}

