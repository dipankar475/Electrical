function approveTransaction(id) {
  const requests = JSON.parse(localStorage.getItem('electro-payment-requests') || '[]');
  const idx = requests.findIndex(r => r.id === id);
  if (idx === -1) return;

  const req = requests[idx];
  req.status = 'approved';
  
  const approvedEmails = JSON.parse(localStorage.getItem('electro-approved-emails') || '[]');
  if (!approvedEmails.includes(req.email)) {
    approvedEmails.push(req.email);
  }
  
  localStorage.setItem('electro-payment-requests', JSON.stringify(requests));
  localStorage.setItem('electro-approved-emails', JSON.stringify(approvedEmails));

  showToast(`Transaction approved for ${req.email}!`, 'success');
  renderAdminPanel();
  
  setTimeout(() => {
    alert(`[Email Notification Dispatch]\nTo: ${req.email}\nSubject: ElectroAudit Pro Premium Unlocked!\n\nDear Auditor,\nYour payment of Rs. 50.00 (Ref ID: ${req.refId}) has been verified and approved by the administrator.\nPremium features are now fully unlocked for your account.`);
  }, 200);
}

function rejectTransaction(id) {
  const requests = JSON.parse(localStorage.getItem('electro-payment-requests') || '[]');
  const idx = requests.findIndex(r => r.id === id);
  if (idx === -1) return;

  const req = requests[idx];
  req.status = 'rejected';
  
  localStorage.setItem('electro-payment-requests', JSON.stringify(requests));

  showToast(`Transaction rejected for ${req.email}.`, 'info');
  renderAdminPanel();
  
  setTimeout(() => {
    alert(`[Email Notification Dispatch]\nTo: ${req.email}\nSubject: ElectroAudit Pro Payment Refused\n\nDear Auditor,\nYour payment submission (Ref ID: ${req.refId}) could not be verified by the administrator.\nPlease verify the transaction reference number and try submitting it again in the payment interface.`);
  }, 200);
}

